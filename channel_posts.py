def load_sent_indices() -> list:
    if os.path.exists(SENT_FILE):
        try:
            with open(SENT_FILE, "r", encoding="utf-8") as f:
                return json.load(f)
        except Exception:
            return []
    return []


def save_sent_indices(sent: list):
    with open(SENT_FILE, "w", encoding="utf-8") as f:
        json.dump(sent, f)


def pick_messages(count: int = 10) -> list:
    total = len(MESSAGES)
    all_indices = list(range(total))

    edu_indices = [i for i in all_indices if MESSAGES[i]["educational"]]
    ent_indices = [i for i in all_indices if not MESSAGES[i]["educational"]]

    sent = load_sent_indices()

    def available(pool):
        avail = [i for i in pool if i not in sent]
        if len(avail) < (1 if pool == ent_indices else count):
            last_buffer = sent[-(len(pool) // 2):]
            avail = [i for i in pool if i not in last_buffer]
        return avail

    avail_edu = available(edu_indices)
    avail_ent = available(ent_indices)

    include_entertainment = len(avail_ent) > 0 and random.random() < 0.5

    selected = []

    if include_entertainment:
        ent_pick = random.choice(avail_ent)
        selected.append(ent_pick)
        avail_edu = [i for i in avail_edu if i != ent_pick]
        edu_count = count - 1
    else:
        edu_count = count

    edu_picks = random.sample(avail_edu, min(edu_count, len(avail_edu)))
    selected.extend(edu_picks)

    random.shuffle(selected)

    sent.extend(selected)
    if len(sent) > total * 2:
        sent = sent[-total:]
    save_sent_indices(sent)

    return [MESSAGES[i]["text"] for i in selected]


async def post_to_channel(bot, delay_seconds: float = 4.0):
    messages = pick_messages(10)
    sent_count = 0
    for text in messages:
        try:
            await bot.send_message(
                chat_id=CHANNEL_ID,
                text=text,
                parse_mode="HTML",
            )
            sent_count += 1
            await asyncio.sleep(delay_seconds)
        except Exception as e:
            print(f"⚠️ فشل إرسال رسالة للقناة: {e}")
    print(f"✅ تم إرسال {sent_count} رسالة للقناة @Ahmad_Naguib_Al")
