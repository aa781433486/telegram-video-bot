def load_sent_aymen():
    if not os.path.exists(SENT_FILE_AYMEN):
        return []
    with open(SENT_FILE_AYMEN, "r", encoding="utf-8") as f:
        return json.load(f)


def save_sent_aymen(data):
    with open(SENT_FILE_AYMEN, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


async def post_to_aymen_channel(bot):
    sent = load_sent_aymen()
    count = 0
    messages_sent = []

    all_content = []

    unsent_jokes = [j for j in JOKES if j not in sent]
    if len(unsent_jokes) < 3:
        sent = [s for s in sent if s not in JOKES]
        unsent_jokes = list(JOKES)

    unsent_quotes = [q for q in QUOTES if q not in sent]
    if len(unsent_quotes) < 2:
        sent = [s for s in sent if s not in QUOTES]
        unsent_quotes = list(QUOTES)

    # Pick 6 jokes + 3 quotes + 1 wallpaper description = 10
    selected_jokes = random.sample(unsent_jokes, min(6, len(unsent_jokes)))
    selected_quotes = random.sample(unsent_quotes, min(3, len(unsent_quotes)))
    selected_wallpaper = random.choice(AI_WALLPAPER_DESCRIPTIONS)

    all_content = selected_jokes + selected_quotes + [selected_wallpaper]
    random.shuffle(all_content)
    all_content = all_content[:10]

    for msg in all_content:
        try:
            await bot.send_message(
                chat_id=CHANNEL_ID,
                text=msg,
                parse_mode="Markdown",
            )
            messages_sent.append(msg)
            count += 1
            import asyncio
            await asyncio.sleep(2)
        except Exception as e:
            print(f"خطأ في إرسال رسالة لقناة أيمن: {e}")

    sent.extend(messages_sent)
    save_sent_aymen(sent)
    print(f"✅ تم إرسال {count} رسائل إلى {CHANNEL_ID}")
